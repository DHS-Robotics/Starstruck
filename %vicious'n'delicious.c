task usercontrol() {
  bool clawClosed = false;

  while (true) {
    driveRuntime(drive);

    takeInput(lift);

		//claw
    if (vexRT[clawInBtn] == 1) {
      clawClosed = true;
      setPower(claw, 127);
    } else if (vexRT[clawOutBtn] == 1) {
      clawClosed = false;
      setPower(claw, -127);
    } else {
      setPower(claw, clawClosed/*&&potentiometerVal(claw)<clawClosedCutoff*/ ? clawStillSpeed : 0);
    }
  }
}
